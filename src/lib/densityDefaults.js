function normalizedCode(code) {
  return String(code || '').trim().toUpperCase().replace(/\s+/g, '');
}

export function defaultDensityForProduct(product) {
  const stored = Number(product?.densityGml || 0);
  if (stored > 0) return stored;
  const byCode = DENSITY_BY_CODE[normalizedCode(product?.code)];
  if (byCode > 0) return byCode;
  const text = `${product?.series || ''} ${product?.name || ''}`;
  if (/纯露|水剂/.test(text)) return 1;
  if (/基础油/.test(text)) return 0.92;
  if (/乳霜/.test(text)) return 1;
  return 0.9;
}
