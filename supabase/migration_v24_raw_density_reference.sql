-- 只填补空值，保留管理员已经录入/确认的密度。
UPDATE public.products p
SET density_g_ml = coalesce(p.density_g_ml, d.density_g_ml),
    density_temperature_c = coalesce(p.density_temperature_c, 20),
    density_source = CASE
      WHEN nullif(trim(coalesce(p.density_source, '')), '') IS NULL THEN d.source
      ELSE p.density_source
    END,
    density_status = CASE
      WHEN p.density_g_ml IS NULL OR p.density_status = 'UNSET' THEN 'REFERENCE'
      ELSE p.density_status
    END
FROM zidu_density_defaults d
WHERE p.channel = 'RAW'
  AND trim(p.code) = trim(d.code);

-- 有 ml/L 销售规格的原料必须已经有自己的密度。若未来新增商品时漏填，
-- 整个迁移会明确失败，避免悄悄使用 1.0 或其他通用值造成库存误差。
DO $$
DECLARE missing TEXT;
BEGIN
  SELECT string_agg(p.code || ' ' || p.name, '；' ORDER BY p.code)
  INTO missing
  FROM public.products p
  WHERE p.channel = 'RAW'
    AND p.density_g_ml IS NULL
    AND EXISTS (
      SELECT 1 FROM public.product_specs s
      WHERE s.product_id = p.id
        AND s.spec ~* '^[[:space:]]*[0-9]+([.][0-9]+)?[[:space:]]*(ml|毫升|l|升)'
    );

  IF missing IS NOT NULL THEN
    RAISE EXCEPTION '以下含ml/L规格的产品没有参考密度：%', missing;
  END IF;
END $$;

